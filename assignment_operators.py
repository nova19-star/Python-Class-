# Assignmenet Operators

# Simple Assignment (=)
a = 5  
print(a)  # Output: 5

# Addition Assignment (+=)
a = 5
a += 3  # Equivalent to a = a + 3
print(a)  # Output: 8

# Subtraction Assignment (-=)
a = 5
a -= 2  # Equivalent to a = a - 2
print(a)  # Output: 3

# Multiplication Assignment (*=)
a = 5
a *= 4  # Equivalent to a = a * 4
print(a)  # Output: 20

# Division Assignment (/=)
a = 10
a /= 2  # Equivalent to a = a / 2
print(a)  # Output: 5.0

# Modulus Assignment (%=)
a = 10
a %= 3  # Equivalent to a = a % 3
print(a)  # Output: 1

# Floor Division Assignment (//=)
a = 10
a //= 3  # Equivalent to a = a // 3
print(a)  # Output: 3

# Exponentiation Assignment (**=)
a = 2
a **= 3  # Equivalent to a = a ** 3
print(a)  # Output: 8

